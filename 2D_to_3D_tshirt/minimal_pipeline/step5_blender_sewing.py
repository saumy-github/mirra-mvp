def load_seam_definitions() -> dict:
    """
    Load seam definitions from seams.json (ENHANCED VERSION).
    
    The seams.json file contains:
    - seams: Simple pairs ["Panel:edge", "Panel:edge"] for quick matching
    - seam_details: Detailed metadata (name, type, priority)
    - panels: Edge vertex coordinates for each panel
    
    Returns:
        Full seam data dictionary, or empty dict if file not found
    """
    global SEAM_DATA
    
    if not SEAM_FILE.exists():
        print(f"  ⚠ No seam file found at {SEAM_FILE}")
        print("    Sewing will use default edge matching")
        return {}
    
    try:
        with open(SEAM_FILE, 'r') as f:
            data = json.load(f)
        
        # Store in global for access by other functions
        SEAM_DATA["seams"] = data.get("seams", [])
        SEAM_DATA["seam_details"] = data.get("seam_details", [])
        SEAM_DATA["panels"] = data.get("panels", {})
        SEAM_DATA["loaded"] = True
        
        print(f"  ✓ Loaded seams.json:")
        print(f"      - {len(SEAM_DATA['seams'])} seam pairs")
        print(f"      - {len(SEAM_DATA['panels'])} panel definitions")
        
        # Log panel edge counts
        for panel_name, panel_data in SEAM_DATA["panels"].items():
            edge_count = len(panel_data.get("edges", {}))
            print(f"      - {panel_name}: {edge_count} edges")
        
        return data
        
    except Exception as e:
        print(f"  ✗ Error loading seams.json: {e}")
        return {}


def parse_seam_pair(seam_pair: str) -> tuple:
    """
    Parse a seam pair string into (panel_name, edge_name).
    
    Format: "Panel_Name:edge_name" -> ("Panel_Name", "edge_name")
    
    Args:
        seam_pair: String like "Front_Panel:side_seam"
    
    Returns:
        Tuple of (panel_name, edge_name)
    """
    if ":" not in seam_pair:
        return (seam_pair, "unknown")
    
    parts = seam_pair.split(":", 1)
    return (parts[0].strip(), parts[1].strip())


def get_panel_edge_vertices(panel_name: str, edge_name: str) -> list:
    """
    Get vertex coordinates for a specific edge from loaded seam data.
    
    Args:
        panel_name: Name of the panel (e.g., "front", "back", "sleeve")
        edge_name: Name of the edge (e.g., "side_seam", "shoulder")
    
    Returns:
        List of [x, y] coordinates in cm, or empty list if not found
    """
    if not SEAM_DATA["loaded"]:
        return []
    
    # Try exact match first
    panel_data = SEAM_DATA["panels"].get(panel_name)
    
    # Try lowercase match
    if not panel_data:
        for key, data in SEAM_DATA["panels"].items():
            if key.lower() == panel_name.lower():
                panel_data = data
                break
    
    if not panel_data:
        return []
    
    edges = panel_data.get("edges", {})
    edge_data = edges.get(edge_name, {})
    
    return edge_data.get("vertices", [])


