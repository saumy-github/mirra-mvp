def apply_simulation_as_mesh(garment: bpy.types.Object, frame: int = None) -> bpy.types.Object:
    """
    Apply the simulated cloth state as a static mesh.
    
    This "freezes" the simulation at a specific frame,
    converting the dynamic cloth to a static mesh that can be exported.
    
    Args:
        garment: The garment with cloth simulation
        frame: Frame to freeze at (default: last frame)
    
    Returns:
        The static mesh object (original is preserved)
    """
    print("→ Applying simulation as static mesh...")
    
    # Go to the target frame
    if frame is None:
        frame = bpy.context.scene.frame_end
    
    bpy.context.scene.frame_set(frame)
    print(f"  Using frame {frame}")
    
    # Select the garment
    bpy.ops.object.select_all(action='DESELECT')
    garment.select_set(True)
    bpy.context.view_layer.objects.active = garment
    
    # Duplicate the object
    bpy.ops.object.duplicate()
    static_garment = bpy.context.active_object
    static_garment.name = f"{garment.name}_Static"
    
    # Apply the cloth modifier (converts to static mesh)
    for mod in static_garment.modifiers:
        if mod.type == 'CLOTH':
            try:
                bpy.ops.object.modifier_apply(modifier=mod.name)
                print(f"  ✓ Applied cloth modifier")
            except Exception as e:
                print(f"  ⚠ Could not apply modifier: {e}")
                # Alternative: convert to mesh
                bpy.ops.object.convert(target='MESH')
    
    # Hide the original animated version
    garment.hide_viewport = True
    garment.hide_render = True
    
    print(f"  ✓ Created static mesh: {static_garment.name}")
    
    return static_garment


def export_garment(garment: bpy.types.Object, 
                   output_dir: str,
                   formats: list = None):
    """
    Export the garment mesh in multiple formats.
    
    Supported formats:
    - OBJ: Universal, widely supported
    - FBX: Good for game engines
    - GLB/GLTF: Web-friendly, includes materials
    
    Args:
        garment: The mesh object to export
        output_dir: Directory to save exports
        formats: List of formats to export (default: all)
    """
    if formats is None:
        formats = ["obj", "fbx", "glb"]
    
    print(f"→ Exporting garment to {output_dir}...")
    
    # Create output directory if needed
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    # Select only the garment
    bpy.ops.object.select_all(action='DESELECT')
    garment.select_set(True)
    bpy.context.view_layer.objects.active = garment
    
    base_name = garment.name.replace(" ", "_")
    
    for fmt in formats:
        filepath = str(output_path / f"{base_name}.{fmt}")
        
        try:
            if fmt.lower() == "obj":
                bpy.ops.wm.obj_export(
                    filepath=filepath,
                    export_selected_objects=True,
                    export_materials=True
                )
                print(f"  ✓ Exported: {base_name}.obj")
                
            elif fmt.lower() == "fbx":
                bpy.ops.export_scene.fbx(
                    filepath=filepath,
                    use_selection=True,
                    mesh_smooth_type='FACE'
                )
                print(f"  ✓ Exported: {base_name}.fbx")
                
            elif fmt.lower() in ["glb", "gltf"]:
                bpy.ops.export_scene.gltf(
                    filepath=filepath,
                    use_selection=True,
                    export_format='GLB' if fmt.lower() == "glb" else 'GLTF_SEPARATE'
                )
                print(f"  ✓ Exported: {base_name}.{fmt}")
                
        except Exception as e:
            print(f"  ✗ Failed to export {fmt}: {e}")
    
    print(f"  ✓ Export complete")


