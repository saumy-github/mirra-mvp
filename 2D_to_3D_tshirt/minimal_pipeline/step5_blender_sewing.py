    panel_height = front_dims.y
    
    # Position FRONT (left side, facing right, LIFTED)
    front.location = Vector((-panel_width * 0.6, 0, lift_height))
    front.rotation_euler = (math.radians(90), 0, 0)  # Stand up vertically
    print(f"  ✓ Front positioned at z={lift_height}m")
    
    # Position BACK (right side, facing left - mirror of front, LIFTED)
    back.location = Vector((panel_width * 0.6, 0, lift_height))
    back.rotation_euler = (math.radians(90), 0, math.radians(180))  # Rotated to face front
    print(f"  ✓ Back positioned at z={lift_height}m")
    
    # Position LEFT SLEEVE (top, LIFTED)
    if left_sleeve:
        sleeve_width = left_sleeve.dimensions.x
        left_sleeve.location = Vector((0, panel_width * 0.5, lift_height + panel_height * 0.2))
        left_sleeve.rotation_euler = (math.radians(90), 0, math.radians(-90))
