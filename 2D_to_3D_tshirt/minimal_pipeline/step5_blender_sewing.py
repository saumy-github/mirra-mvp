    group_name = f"sew_{edge_type}"
    
    # Add vertex group to first object
    if group_name not in obj1.vertex_groups:
        vg1 = obj1.vertex_groups.new(name=group_name)
    else:
        vg1 = obj1.vertex_groups[group_name]
    
