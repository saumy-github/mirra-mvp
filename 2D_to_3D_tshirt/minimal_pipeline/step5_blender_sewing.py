def run_simulation(frames: int = 150):
    """
    Run the cloth simulation.
    
    This bakes the simulation - calculating cloth position
    for each frame. The sewing springs pull edges together
    while gravity pulls the fabric down.
    
    Args:
        frames: Number of frames to simulate
    """
    print(f"→ Running simulation for {frames} frames...")
    print("  (This may take a few minutes)")
    
    # Set frame range
    bpy.context.scene.frame_end = frames
    
    # Go to frame 1
    bpy.context.scene.frame_set(1)
    
    # Bake simulation
    # In Blender UI this would be: Physics > Cloth > Cache > Bake
    # Programmatically, we just step through frames
    
    for frame in range(1, frames + 1):
        bpy.context.scene.frame_set(frame)
        
        # Print progress every 30 frames
        if frame % 30 == 0:
            print(f"  Frame {frame}/{frames}...")
    
    print(f"  ✓ Simulation complete")


def bake_simulation(garment: bpy.types.Object, frames: int = 60):
    """
    Bake the cloth simulation to cache.
    
    Baking pre-calculates all simulation frames so playback is instant.
    This is more reliable than stepping through frames manually.
    
    60 frames is sufficient for memory-constrained systems:
    - Sewing springs to pull edges together (~20-30 frames)
    - Fabric settling (~30-50 frames)
    - Final stabilization (~50-60 frames)
    
    Args:
        garment: The garment object with cloth modifier
        frames: Number of frames to bake (default: 120)
    
    Returns:
        True if bake succeeded, False otherwise
    """
    print(f"\n→ Baking cloth simulation ({frames} frames)...")
    print("  This may take several minutes...")
    print("  Progress:")
    
    # Find the cloth modifier
    cloth_mod = None
    for mod in garment.modifiers:
        if mod.type == 'CLOTH':
            cloth_mod = mod
            break
    
    if not cloth_mod:
        print("  ✗ No cloth modifier found!")
        return False
    
    # Set frame range for scene
    bpy.context.scene.frame_start = 1
    bpy.context.scene.frame_end = frames
    
    # Configure cache for baking
    point_cache = cloth_mod.point_cache
    point_cache.frame_start = 1
    point_cache.frame_end = frames
    
    # Select the garment
    bpy.ops.object.select_all(action='DESELECT')
    garment.select_set(True)
    bpy.context.view_layer.objects.active = garment
    
    # Bake the simulation using Blender's built-in operator
    try:
        # Free any existing bake first
        bpy.ops.ptcache.free_bake_all()
        
        # Bake all physics caches
        bpy.ops.ptcache.bake_all(bake=True)
        
        print(f"  ✓ Simulation baked successfully ({frames} frames)")
        return True
        
    except Exception as e:
        print(f"  ⚠ Bake operator failed: {e}")
        print("  Falling back to frame-by-frame bake...")
        
        # Fallback: step through frames manually
        # This forces Blender to calculate each frame
        for frame in range(1, frames + 1):
            bpy.context.scene.frame_set(frame)
            
            # Print progress every 20 frames
            if frame % 20 == 0:
                progress = (frame / frames) * 100
                print(f"    Frame {frame}/{frames} ({progress:.0f}%)")
        
        print(f"  ✓ Frame-by-frame bake complete ({frames} frames)")
        return True


def setup_simulation_cache(garment: bpy.types.Object, 
                            frames: int = 60,
                            cache_step: int = 1):
    """
    Configure the simulation cache settings before baking.
    
    Args:
        garment: Garment with cloth modifier
        frames: Total simulation frames
        cache_step: Frame step for caching (1 = every frame)
    """
    # Find cloth modifier
    cloth_mod = None
    for mod in garment.modifiers:
        if mod.type == 'CLOTH':
            cloth_mod = mod
            break
    
    if not cloth_mod:
        return
    
    # Configure point cache
    cache = cloth_mod.point_cache
    cache.frame_start = 1
    cache.frame_end = frames
    if hasattr(cache, "step"):
        cache.step = cache_step
    cache.use_disk_cache = True
    
    # Use disk cache for large simulations
   
    
    print(f"  ✓ Cache configured: frames 1-{frames}, step={cache_step}")


def auto_bake_cloth_simulation(garment: bpy.types.Object,
                                frames: int = 60,
                                quality: int = 8) -> bool:
    """
    Automatically configure and bake cloth simulation.
    
    This is the "one-click" bake function that:
    1. Verifies cloth modifier exists
    2. Sets optimal simulation parameters
    3. Configures cache
    4. Bakes the simulation
    5. Jumps to final frame
    
    Args:
        garment: The garment mesh with cloth modifier
        frames: Number of frames to simulate (default: 120)
        quality: Simulation quality steps (default: 12)
    
    Returns:
        True if bake succeeded
    """
    print("\n" + "="*50)
    print("   AUTO-BAKE CLOTH SIMULATION")
    print("="*50)
    
    # Verify garment has cloth modifier
    cloth_mod = None
    for mod in garment.modifiers:
        if mod.type == 'CLOTH':
            cloth_mod = mod
            break
    
    if not cloth_mod:
        print("  ✗ ERROR: No cloth modifier found on garment")
        print("    Add cloth modifier first with add_cloth_modifier()")
        return False
    
    # Verify sewing springs are enabled
    if not cloth_mod.settings.use_sewing_springs:
        print("  ⚠ WARNING: Sewing springs not enabled!")
        print("    Enabling sewing springs...")
        cloth_mod.settings.use_sewing_springs = True
        cloth_mod.settings.sewing_force_max = SEWING_SETTINGS["force"]
    
    # Set quality
    cloth_mod.settings.quality = quality
    
    # Set scene frame range
    bpy.context.scene.frame_start = 1
