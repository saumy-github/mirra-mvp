def find_vertices_near_coordinates(mesh_obj: bpy.types.Object, 
                                    target_coords: list,
                                    tolerance: float = 0.05) -> list:
    """
    Find mesh vertices that are near the given target coordinates.
    
    This matches pattern edge vertices (from seams.json) to actual
    Blender mesh vertices after subdivision.
    
    Args:
        mesh_obj: The Blender mesh object
        target_coords: List of [x, y] coordinates in cm from seams.json
        tolerance: Distance tolerance in meters for matching
    
    Returns:
        List of vertex indices that match the target coordinates
    """
    mesh = mesh_obj.data
    matrix = mesh_obj.matrix_world
    
    matched_indices = []
    
    for i, vert in enumerate(mesh.vertices):
        world_co = matrix @ Vector(vert.co)
        
        # Check against each target coordinate
        for tx, ty in target_coords:
            # Convert cm to meters for comparison
            target_x = tx * CM_TO_M
            target_y = ty * CM_TO_M
            
            # Calculate distance (using x,y as mesh is initially flat in XY plane)
            # After rotation, we check in world space
            dist_xy = math.sqrt((world_co.x - target_x)**2 + (world_co.y - target_y)**2)
            dist_xz = math.sqrt((world_co.x - target_x)**2 + (world_co.z - target_y)**2)
            
            # Match if close to target in either XY or XZ plane (depending on rotation)
            if dist_xy < tolerance or dist_xz < tolerance:
                if i not in matched_indices:
                    matched_indices.append(i)
                break
    
    return matched_indices


def find_edge_indices_by_position(mesh_obj: bpy.types.Object,
                                   edge_vertices: list,
                                   tolerance: float = 0.03) -> list:
    """
    Find mesh edge indices that correspond to a pattern edge.
    
    After subdivision, we need to find which mesh edges
    lie along the original pattern edge path.
    
    Args:
        mesh_obj: The Blender mesh object
        edge_vertices: List of [x, y] coordinates defining the edge path
        tolerance: Distance tolerance for matching
    
    Returns:
        List of mesh edge indices
    """
    if not edge_vertices or len(edge_vertices) < 2:
        return []
    
    mesh = mesh_obj.data
    matrix = mesh_obj.matrix_world
    
    # Convert edge vertices to world space line segments
    edge_path = []
    for x, y in edge_vertices:
        edge_path.append(Vector((x * CM_TO_M, y * CM_TO_M, 0)))
    
    matched_edges = []
    
    for edge_idx, edge in enumerate(mesh.edges):
        v1 = matrix @ Vector(mesh.vertices[edge.vertices[0]].co)
        v2 = matrix @ Vector(mesh.vertices[edge.vertices[1]].co)
        midpoint = (v1 + v2) / 2
        
        # Check if edge midpoint is close to any segment of the path
        for i in range(len(edge_path) - 1):
            p1, p2 = edge_path[i], edge_path[i + 1]
            
            # Point-to-line-segment distance (simplified 2D)
            dist = point_to_segment_distance_2d(
                (midpoint.x, midpoint.z),  # Use X,Z after rotation
                (p1.x, p1.y), (p2.x, p2.y)
            )
            
            if dist < tolerance:
                matched_edges.append(edge_idx)
                break
    
    return matched_edges


def point_to_segment_distance_2d(point: tuple, seg_start: tuple, seg_end: tuple) -> float:
    """
    Calculate distance from a point to a line segment in 2D.
    
    Args:
        point: (x, y) point
        seg_start: (x, y) segment start
        seg_end: (x, y) segment end
    
    Returns:
        Distance from point to nearest point on segment
    """
    px, py = point
    x1, y1 = seg_start
    x2, y2 = seg_end
    
    # Vector from seg_start to seg_end
    dx = x2 - x1
    dy = y2 - y1
    
    # Handle zero-length segment
    seg_len_sq = dx * dx + dy * dy
    if seg_len_sq < 1e-10:
        return math.sqrt((px - x1)**2 + (py - y1)**2)
    
    # Project point onto line, clamped to segment
    t = max(0, min(1, ((px - x1) * dx + (py - y1) * dy) / seg_len_sq))
    
    # Nearest point on segment
    nearest_x = x1 + t * dx
    nearest_y = y1 + t * dy
    
    return math.sqrt((px - nearest_x)**2 + (py - nearest_y)**2)


def select_edges_by_indices(mesh_obj: bpy.types.Object, edge_indices: list):
    """
    Select specific edges in a mesh by their indices.
    
    Args:
        mesh_obj: The mesh object
        edge_indices: List of edge indices to select
    """
    # Ensure we're in object mode first
    if bpy.context.active_object and bpy.context.active_object.mode != 'OBJECT':
        bpy.ops.object.mode_set(mode='OBJECT')
    
    # Select the target object
    bpy.ops.object.select_all(action='DESELECT')
    mesh_obj.select_set(True)
    bpy.context.view_layer.objects.active = mesh_obj
    
    # Enter edit mode
    bpy.ops.object.mode_set(mode='EDIT')
    
    # Deselect all first
    bpy.ops.mesh.select_all(action='DESELECT')
    
    # Switch to edge select mode
    bpy.ops.mesh.select_mode(type='EDGE')
    
    # Return to object mode to manipulate selection data
    bpy.ops.object.mode_set(mode='OBJECT')
    
    # Select edges by index
    mesh = mesh_obj.data
    for idx in edge_indices:
        if 0 <= idx < len(mesh.edges):
            mesh.edges[idx].select = True
    
    # Back to edit mode to see selection
    bpy.ops.object.mode_set(mode='EDIT')


def mark_edges_as_seam(mesh_obj: bpy.types.Object, edge_indices: list, seam_name: str = ""):
    """
    Mark specific edges as seams in Blender.
    
    Seam marking tells Blender's cloth solver which edges
    should be pulled together during simulation.
    
    Args:
        mesh_obj: The mesh object
        edge_indices: List of edge indices to mark as seams
        seam_name: Optional name for logging
    """
    if not edge_indices:
        print(f"    ⚠ No edges to mark for seam {seam_name}")
        return
    
    # Ensure object mode
    if bpy.context.active_object and bpy.context.active_object.mode != 'OBJECT':
        bpy.ops.object.mode_set(mode='OBJECT')
    
    # Select object
    bpy.ops.object.select_all(action='DESELECT')
    mesh_obj.select_set(True)
    bpy.context.view_layer.objects.active = mesh_obj
    
    # Use BMesh for seam marking
    bpy.ops.object.mode_set(mode='EDIT')
    bm = bmesh.from_edit_mesh(mesh_obj.data)
    bm.edges.ensure_lookup_table()
    
    marked_count = 0
    for idx in edge_indices:
        if 0 <= idx < len(bm.edges):
            bm.edges[idx].seam = True
            marked_count += 1
    
    # Update mesh
    bmesh.update_edit_mesh(mesh_obj.data)
    bpy.ops.object.mode_set(mode='OBJECT')
    
    if seam_name:
        print(f"    ✓ Marked {marked_count} edges as seam: {seam_name}")


def mark_seams_from_vertex_group(mesh_obj: bpy.types.Object, vertex_group_name: str):
    """
    Mark edges as seams if both vertices are in the given vertex group.
    
    This uses the vertex groups created during seam setup.
    
    Args:
        mesh_obj: The mesh object with vertex groups
        vertex_group_name: Name of the vertex group
    """
    if vertex_group_name not in mesh_obj.vertex_groups:
        print(f"    ⚠ Vertex group not found: {vertex_group_name}")
        return
    
    vg = mesh_obj.vertex_groups[vertex_group_name]
    vg_index = vg.index
    
    # Get vertices in this group
    verts_in_group = set()
    for v in mesh_obj.data.vertices:
        for g in v.groups:
            if g.group == vg_index and g.weight > 0.5:
                verts_in_group.add(v.index)
                break
    
    if not verts_in_group:
        return
    
    # Find edges where both vertices are in the group
    edge_indices = []
    for edge in mesh_obj.data.edges:
        if edge.vertices[0] in verts_in_group and edge.vertices[1] in verts_in_group:
            edge_indices.append(edge.index)
    
    # Mark these edges as seams
    mark_edges_as_seam(mesh_obj, edge_indices, vertex_group_name)


def enable_sewing_springs_on_cloth(cloth_obj: bpy.types.Object, 
                                    sewing_force: float = 25.0,
                                    shrink_min: float = 0.0,
                                    max_sewing_dist: float = 0.15):
    """
    Enable and configure sewing springs on a cloth modifier.
    
    Sewing springs pull marked seam edges together during simulation.
    This is the key feature for realistic garment construction.
    
    Args:
        cloth_obj: Object with cloth modifier
        sewing_force: Strength of sewing pull (20-30 recommended)
        shrink_min: Minimum shrink factor (0.0 = no extra shrink)
        max_sewing_dist: Maximum distance for sewing to work (meters)
    
    Returns:
        True if sewing enabled, False if no cloth modifier found
    """
    print(f"→ Enabling sewing springs on {cloth_obj.name}...")
    
    # Find cloth modifier
    cloth_mod = None
    for mod in cloth_obj.modifiers:
        if mod.type == 'CLOTH':
            cloth_mod = mod
            break
    
    if not cloth_mod:
        print(f"  ✗ No cloth modifier found on {cloth_obj.name}")
        return False
    
    # Enable sewing springs
    cloth_settings = cloth_mod.settings
    cloth_settings.use_sewing_springs = True
    cloth_settings.sewing_force_max = sewing_force
    cloth_settings.shrink_min = shrink_min
    
    # Note: max_sewing_dist is not directly exposed in newer Blender versions
    # The sewing works based on vertex groups instead
    
    print(f"  ✓ Sewing springs ENABLED:")
    print(f"      - sewing_force_max: {sewing_force}")
    print(f"      - shrink_min: {shrink_min}")
    print(f"      - use_sewing_springs: True")
    
    return True


def mark_all_seam_edges(panels: dict):
    """
    Mark seam edges on all panels based on loaded seam data.
    
    This walks through all seam pairs and marks corresponding
    edges on the panel meshes.
    
    Args:
        panels: Dictionary of panel mesh objects
    """
    if not SEAM_DATA["loaded"]:
        print("  ⚠ No seam data loaded, skipping edge marking")
        return
    
    print("→ Marking seam edges on panel meshes...")
    
    # Resolve panel names to objects
    panel_map = resolve_panel_objects(panels)
    
    # Process each seam pair
    for seam_pair in SEAM_DATA["seams"]:
        if not isinstance(seam_pair, list) or len(seam_pair) != 2:
            continue
        
        # Parse both sides of the seam
        panel1_name, edge1_name = parse_seam_pair(seam_pair[0])
        panel2_name, edge2_name = parse_seam_pair(seam_pair[1])
        
        # Get panel objects
        obj1 = panel_map.get(panel1_name)
        obj2 = panel_map.get(panel2_name)
        
        if not obj1:
            # Try lowercase
            for key, obj in panel_map.items():
                if key.lower() == panel1_name.lower():
                    obj1 = obj
                    break
        
        if not obj2:
            for key, obj in panel_map.items():
                if key.lower() == panel2_name.lower():
                    obj2 = obj
                    break
        
        # Get edge vertex coordinates from seam data
        edge1_verts = get_panel_edge_vertices(panel1_name, edge1_name)
        edge2_verts = get_panel_edge_vertices(panel2_name, edge2_name)
        
        # Find and mark edges on each panel
        if obj1 and edge1_verts:
            edge_indices = find_edge_indices_by_position(obj1, edge1_verts)
            if edge_indices:
                mark_edges_as_seam(obj1, edge_indices, f"{panel1_name}:{edge1_name}")
        
        if obj2 and edge2_verts:
            edge_indices = find_edge_indices_by_position(obj2, edge2_verts)
            if edge_indices:
                mark_edges_as_seam(obj2, edge_indices, f"{panel2_name}:{edge2_name}")
    
    print(f"  ✓ Seam edge marking complete")


def get_edge_vertices_by_direction(mesh_obj: bpy.types.Object, 
                                    direction: str,
                                    tolerance: float = 0.02) -> list:
    """
    Robust vertex selector that handles CURVES (like armholes) using 
    Sector Logic instead of just Bounding Box Logic.
    """
    mesh = mesh_obj.data
    matrix = mesh_obj.matrix_world
    
    # Get Bounds
    coords = [matrix @ Vector(v.co) for v in mesh.vertices]
    if not coords: return []
    
    min_x, max_x = min(c.x for c in coords), max(c.x for c in coords)
    min_y, max_y = min(c.y for c in coords), max(c.y for c in coords)
    min_z, max_z = min(c.z for c in coords), max(c.z for c in coords)
    
    width = max_x - min_x
    height = max_z - min_z  # Assuming Z is up (standing) or Y is up (flat)
    
    edge_verts = []
    
    for i, v in enumerate(mesh.vertices):
        co = matrix @ Vector(v.co)
        
        # --- LOGIC UPGRADE: SECTORS ---
        # Instead of just "Left Edge", we define "Zones"
        
        match = False
        
        # 1. Straight Edges (Classic Bounding Box)
        if direction == "left" and abs(co.x - min_x) < tolerance: match = True
        elif direction == "right" and abs(co.x - max_x) < tolerance: match = True
        elif direction == "top" and abs(co.z - max_z) < tolerance: match = True
        elif direction == "bottom" and abs(co.z - min_z) < tolerance: match = True
        
        # 2. Curved Zones (The Fix for Armholes/Necks)
        # Armhole Left: Upper-Left Quadrant, but NOT the shoulder top or side straight
        elif direction == "armhole_left":
            # Check if vertex is in the top-left 30% of the mesh
            if (co.x < min_x + width * 0.3) and (co.z > max_z - height * 0.4):
                # Exclude the very top (shoulder) and very left (side seam)
                if abs(co.z - max_z) > tolerance and abs(co.x - min_x) > tolerance:
                    match = True

        # Armhole Right: Upper-Right Quadrant
        elif direction == "armhole_right":
            if (co.x > max_x - width * 0.3) and (co.z > max_z - height * 0.4):
                if abs(co.z - max_z) > tolerance and abs(co.x - max_x) > tolerance:
                    match = True

        if match:
            edge_verts.append(i)
            
    return edge_verts


def create_vertex_group_for_edge(obj: bpy.types.Object, 
                                  group_name: str,
                                  vertex_indices: list,
                                  weight: float = 1.0):
    """
    Create or update a vertex group with specified vertices.
    
    Vertex groups are how Blender marks which vertices should be sewn.
    Each sewing edge needs its own vertex group.
    
    Args:
        obj: The mesh object
        group_name: Name for the vertex group
        vertex_indices: List of vertex indices to add
        weight: Weight for the vertices (1.0 = fully included)
    """
    # Get or create vertex group
    if group_name in obj.vertex_groups:
        vg = obj.vertex_groups[group_name]
    else:
        vg = obj.vertex_groups.new(name=group_name)
    
    # Add vertices to group
    vg.add(vertex_indices, weight, 'ADD')
    
    return vg


def setup_seams_from_definitions(panels: dict, seam_defs: list):
    """
    Set up sewing vertex groups based on seam definitions.
    
    This maps the abstract seam definitions to actual vertices in the
    Blender meshes. After subdivision, we find edges by their position
    rather than vertex indices.
    
    Args:
        panels: Dictionary of pattern piece mesh objects
        seam_defs: List of seam definitions from seams.json
    """
    if not seam_defs:
        print("  No seam definitions - using default sewing setup")
        return
    
    print("→ Setting up seams from definitions...")
    
    # Map piece names to objects
    piece_map = {
        "front": panels.get("front"),
        "back": panels.get("back"),
        "sleeve": panels.get("left_sleeve"),  # We'll mirror for right
    }
    
    # Edge direction mapping based on seam names
    # This translates pattern edges to mesh positions
    edge_to_direction = {
        # Front panel edges
        "left_edge": "left",
        "right_edge": "right",
        "shoulder_left": "top",    # Approximation
        "shoulder_right": "top",
        "armhole_left": "left",    # Upper left area
        "armhole_right": "right",  # Upper right area
        
        # Back panel edges (mirror of front)
        
        # Sleeve edges
        "top": "top",
        "bottom": "bottom", 
        "cap": "top",              # Sleeve cap
        "underarm": "bottom",
    }
    
    seam_count = 0
    
    for seam in seam_defs:
        seam_name = seam.get("name", f"seam_{seam_count}")
        edge1 = seam.get("edge1", {})
        edge2 = seam.get("edge2", {})
        
        piece1_name = edge1.get("piece", "")
        piece2_name = edge2.get("piece", "")
        edge1_name = edge1.get("edge_name", "")
        edge2_name = edge2.get("edge_name", "")
        
        # Get mesh objects
        obj1 = piece_map.get(piece1_name)
        obj2 = piece_map.get(piece2_name)
        
        if not obj1 or not obj2:
            print(f"    ⚠ Skipping {seam_name}: piece not found")
            continue
        
        # Get edge directions
        dir1 = edge_to_direction.get(edge1_name, "left")
        dir2 = edge_to_direction.get(edge2_name, "left")
        
        # Find vertices on each edge
        verts1 = get_edge_vertices_by_direction(obj1, dir1)
