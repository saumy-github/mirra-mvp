def load_seam_definitions() -> list:
    """
    Load seam definitions from seams.json.
    
    The seams.json file contains pairs of edges that should be sewn together.
    Each seam has:
    - name: Descriptive name (e.g., "left_side_seam")
    - edge1: {piece, edge_name, vertices} for first edge
    - edge2: {piece, edge_name, vertices} for second edge
    
    Returns:
        List of seam definitions, or empty list if file not found
    """
    if not SEAM_FILE.exists():
        print(f"  ⚠ No seam file found at {SEAM_FILE}")
        print("    Sewing will use default edge matching")
        return []
    
    try:
        with open(SEAM_FILE, 'r') as f:
            data = json.load(f)
        seams = data.get("seams", [])
        print(f"  ✓ Loaded {len(seams)} seam definitions from seams.json")
        return seams
    except Exception as e:
        print(f"  ✗ Error loading seams.json: {e}")
        return []


def get_edge_vertices_by_direction(mesh_obj: bpy.types.Object, 
                                    direction: str,
                                    tolerance: float = 0.02) -> list:
    """
    Robust vertex selector that handles CURVES (like armholes) using 
    Sector Logic instead of just Bounding Box Logic.
    """
    mesh = mesh_obj.data
    matrix = mesh_obj.matrix_world
    
    # Get Bounds
    coords = [matrix @ Vector(v.co) for v in mesh.vertices]
    if not coords: return []
    
    min_x, max_x = min(c.x for c in coords), max(c.x for c in coords)
    min_y, max_y = min(c.y for c in coords), max(c.y for c in coords)
    min_z, max_z = min(c.z for c in coords), max(c.z for c in coords)
    
    width = max_x - min_x
    height = max_z - min_z # Assuming Z is up (standing) or Y is up (flat)
    
    edge_verts = []
    
    for i, v in enumerate(mesh.vertices):
        co = matrix @ Vector(v.co)
        
        # --- LOGIC UPGRADE: SECTORS ---
        # Instead of just "Left Edge", we define "Zones"
        
        match = False
        
        # 1. Straight Edges (Classic Bounding Box)
        if direction == "left" and abs(co.x - min_x) < tolerance: match = True
        elif direction == "right" and abs(co.x - max_x) < tolerance: match = True
        elif direction == "top" and abs(co.z - max_z) < tolerance: match = True
        elif direction == "bottom" and abs(co.z - min_z) < tolerance: match = True
        
        # 2. Curved Zones (The Fix for Armholes/Necks)
        # Armhole Left: Upper-Left Quadrant, but NOT the shoulder top or side straight
        elif direction == "armhole_left":
            # Check if vertex is in the top-left 30% of the mesh
            if (co.x < min_x + width * 0.3) and (co.z > max_z - height * 0.4):
                # Exclude the very top (shoulder) and very left (side seam)
                if abs(co.z - max_z) > tolerance and abs(co.x - min_x) > tolerance:
                    match = True

        # Armhole Right: Upper-Right Quadrant
        elif direction == "armhole_right":
            if (co.x > max_x - width * 0.3) and (co.z > max_z - height * 0.4):
                if abs(co.z - max_z) > tolerance and abs(co.x - max_x) > tolerance:
                    match = True

        if match:
            edge_verts.append(i)
            
    return edge_verts


def create_vertex_group_for_edge(obj: bpy.types.Object, 
                                  group_name: str,
                                  vertex_indices: list,
                                  weight: float = 1.0):
    """
    Create or update a vertex group with specified vertices.
    
    Vertex groups are how Blender marks which vertices should be sewn.
    Each sewing edge needs its own vertex group.
    
    Args:
        obj: The mesh object
        group_name: Name for the vertex group
        vertex_indices: List of vertex indices to add
        weight: Weight for the vertices (1.0 = fully included)
    """
    # Get or create vertex group
    if group_name in obj.vertex_groups:
        vg = obj.vertex_groups[group_name]
    else:
        vg = obj.vertex_groups.new(name=group_name)
    
    # Add vertices to group
    vg.add(vertex_indices, weight, 'ADD')
    
    return vg


def setup_seams_from_definitions(panels: dict, seam_defs: list):
    """
    Set up sewing vertex groups based on seam definitions.
    
    This maps the abstract seam definitions to actual vertices in the
    Blender meshes. After subdivision, we find edges by their position
    rather than vertex indices.
    
    Args:
        panels: Dictionary of pattern piece mesh objects
        seam_defs: List of seam definitions from seams.json
    """
    if not seam_defs:
        print("  No seam definitions - using default sewing setup")
        return
    
    print("→ Setting up seams from definitions...")
    
    # Map piece names to objects - use the panel dictionary keys directly
    piece_map = panels  # panels dict already has "Front_Panel", "Back_Panel", etc.
    
    # Edge direction mapping based on seam names
    # This translates pattern edges to mesh positions
    edge_to_direction = {
        # Front panel edges
        "left_edge": "left",
        "right_edge": "right",
        "shoulder_left": "top",    # Approximation
        "shoulder_right": "top",
        "armhole_left": "left",    # Upper left area
        "armhole_right": "right",  # Upper right area
        
        # Back panel edges (mirror of front)
        
        # Sleeve edges
        "top": "top",
        "bottom": "bottom", 
        "cap": "top",              # Sleeve cap
        "underarm": "bottom",
    }
    
    seam_count = 0
    
    for seam in seam_defs:
        seam_name = seam.get("name", f"seam_{seam_count}")
        
        # NEW FORMAT: panel_a, panel_b, edge_a, edge_b
        panel_a_name = seam.get("panel_a", "")
        panel_b_name = seam.get("panel_b", "")
        edge_a_name = seam.get("edge_a", "")
        edge_b_name = seam.get("edge_b", "")
        
        # Get mesh objects
        obj_a = piece_map.get(panel_a_name)
        obj_b = piece_map.get(panel_b_name)
        
        if not obj_a or not obj_b:
            print(f"    ⚠ Skipping {seam_name}: piece not found (panel_a={panel_a_name}, panel_b={panel_b_name})")
            continue
        
        # Get edge directions
        dir_a = edge_to_direction.get(edge_a_name, "left")
        dir_b = edge_to_direction.get(edge_b_name, "left")
        
        # Find vertices on each edge
        verts_a = get_edge_vertices_by_direction(obj_a, dir_a)
        verts_b = get_edge_vertices_by_direction(obj_b, dir_b)
        
        if verts_a and verts_b:
            # Create vertex groups
            group_name = f"sew_{seam_name}"
            create_vertex_group_for_edge(obj_a, group_name, verts_a)
            create_vertex_group_for_edge(obj_b, group_name, verts_b)
            print(f"    ✓ {seam_name}: {len(verts_a)} ↔ {len(verts_b)} vertices")
            seam_count += 1
        else:
            print(f"    ⚠ {seam_name}: no vertices found (v_a={len(verts_a)}, v_b={len(verts_b)})")
    
    print(f"  ✓ Created {seam_count} seam vertex groups")


def setup_default_seams(panels: dict):
    """
    Set up default sewing seams when no seam definitions are available.
    
    Creates sewing groups for:
    - Side seams (front left ↔ back left, front right ↔ back right)
    - Shoulder seams (front top ↔ back top)
    
    Args:
        panels: Dictionary of pattern piece mesh objects
    """
    print("→ Setting up default sewing seams...")
    
    front = panels.get("front")
    back = panels.get("back")
    
    if not front or not back:
        print("  ⚠ Cannot create seams: front or back panel missing")
        return
    
    # Side seams: connect left edges of front and back
    front_left = get_edge_vertices_by_direction(front, "left")
    back_left = get_edge_vertices_by_direction(back, "left")
    
    if front_left and back_left:
        create_vertex_group_for_edge(front, "sew_left_side", front_left)
        create_vertex_group_for_edge(back, "sew_left_side", back_left)
        print(f"  ✓ Left side seam: {len(front_left)} ↔ {len(back_left)} vertices")
    
    # Right side seam
    front_right = get_edge_vertices_by_direction(front, "right")
    back_right = get_edge_vertices_by_direction(back, "right")
    
    if front_right and back_right:
        create_vertex_group_for_edge(front, "sew_right_side", front_right)
        create_vertex_group_for_edge(back, "sew_right_side", back_right)
        print(f"  ✓ Right side seam: {len(front_right)} ↔ {len(back_right)} vertices")
    
    # Shoulder seams (top edge)
    front_top = get_edge_vertices_by_direction(front, "top")
    back_top = get_edge_vertices_by_direction(back, "top")
    
    if front_top and back_top:
        create_vertex_group_for_edge(front, "sew_shoulders", front_top)
        create_vertex_group_for_edge(back, "sew_shoulders", back_top)
        print(f"  ✓ Shoulder seam: {len(front_top)} ↔ {len(back_top)} vertices")


