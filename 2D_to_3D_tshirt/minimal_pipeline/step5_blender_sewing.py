def setup_sewing_constraint(cloth_obj: bpy.types.Object, vertex_group: str):
    """
    Configure cloth modifier to use sewing.
    
    The sewing constraint in Blender's cloth simulation:
    - Finds vertices in the sewing vertex group
    - Creates springs that pull them toward matching vertices
    - Over simulation time, edges come together like actual sewing
    
    Args:
        cloth_obj: Object with cloth modifier
        vertex_group: Name of vertex group with sewing vertices
    """
    # Get the cloth modifier
    cloth_mod = None
    for mod in cloth_obj.modifiers:
        if mod.type == 'CLOTH':
            cloth_mod = mod
            break
    
    if cloth_mod is None:
        print(f"  ⚠ No cloth modifier on {cloth_obj.name}")
        return
    
    # Enable sewing
    cloth_mod.settings.use_sewing_springs = True
    cloth_mod.settings.sewing_force_max = SEWING_SETTINGS["force"]
    
    # Set the vertex group for sewing
    cloth_mod.settings.vertex_group_mass = vertex_group


def join_pattern_pieces(pieces: list, name: str) -> bpy.types.Object:
    """
    Join multiple pattern pieces into a single object.
    
    For cloth simulation, it's easier to have one object
    with all pattern pieces. Sewing then happens between
    different vertex groups within that object.
    
    Args:
        pieces: List of mesh objects to join
        name: Name for the combined object
    
    Returns:
        The joined object
    """
    print(f"→ Joining pattern pieces into {name}...")
    
    # Deselect all
    bpy.ops.object.select_all(action='DESELECT')
    
    # Select all pieces
    for piece in pieces:
        if piece:
            piece.select_set(True)
    
    # Make first piece active
    bpy.context.view_layer.objects.active = pieces[0]
    
    # Join
    bpy.ops.object.join()
    
    # Rename and verify
    joined = bpy.context.active_object
    joined.name = name
    
    # Force exact naming (in case Blender adds .001 suffix)
    if joined.name != name:
        # Remove any existing object with this name
        existing = bpy.data.objects.get(name)
        if existing and existing != joined:
            bpy.data.objects.remove(existing, do_unlink=True)
        joined.name = name
    
    print(f"  ✓ Created combined garment: {joined.name}")
